 <h2 class="text-center" align="center">NOTICE TO PATIENTS<hr></h2>
<p class="text-center"><strong>THE HOSPITAL PRIVACY PROGRAM</strong></p>
<p>The Hospital collects, uses, discloses and retains personal health information (PHI):</p>
<ul>
<li>To provide you with care;</li>
<li>To monitor and evaluate the quality of care we provide you;</li>
<li>To administer and manage the operations of the hospital;</li>
<li>To do research, educate and collect statistics;</li>
<li>To comply with legal and regulatory requirements.</li>
<li>As part of shared Electronic Health Records programs</a></li>
</ul>
<p>As well, we disclose your contact information to our Foundation, so that they may conduct fundraising to improve our health-care facilities, services and programs.</p>
<p><strong>Access and Correction</strong></p>
<p>The Hospital has an electronic and paper copy of your health record. There are other health-care providers outside of The Hospital who can access your electronic health record but they or their team must be involved in your care and they must sign an agreement with The Hospital.</p>
<p>You may obtain access to or request a correction of your personal health information by contacting the Health Records Department.</p>
<p><strong>Safeguards</strong></p>
<p>We take steps to protect your personal health information from theft, loss and unauthorized access, copying, modification, use, disclosure and disposal.  We conduct audits and complete investigations to monitor and manage our privacy compliance.  We take steps to ensure that everyone who performs services for us protects your privacy and only uses your personal health information for the purposes you have consented to.</p>
<p><strong>Privacy Level Code</strong></p>
<p>Unless you tell us differently, we may let visitors or callers know your location in the hospital and your general health status.  We use different privacy level codes to restrict or minimize who knows you are at the hospital. If you do not want anyone to know you are in hospital, you, or your substitute decision-maker on your behalf, may request that a <strong>Privacy Level Code</strong> be placed in our patient registration system.</p>
<p><strong>Warning Flag</strong></p>
<p>If you have concerns about your personal health information being accessed inappropriately, you can request that a <strong>Warning Flag </strong>be placed on your personal health information in The Hospitalâ€™s electronic health record system. Accesses to Warning Flag files are investigated.</p>
